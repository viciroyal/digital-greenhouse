// Get texture config by crate ID
export const getCrateTexture = (crateId: string): { pattern: string; opacity: number; concept: string } => {
  switch (crateId) {
    case 'root':
      return {
        pattern: BogolanPattern,
        opacity: 0.05, // 5% - Subliminal
        concept: 'The woven earth of the Maroons and Mali.',
      };
    case 'flow':
      return {
        pattern: IncaStonePattern,
        opacity: 0.05, // 5% - Subliminal
        concept: 'The structure of the water and stone.',
      };
    case 'signal':
      return {
        pattern: StarChartPattern,
        opacity: 0.08, // 8% - Visible
        concept: 'The Dogon map of the sky.',
      };
    case 'crown':
      return {
        pattern: FlowerOfLifePattern,
        opacity: 0.10, // 10% - Shimmering
        concept: 'The Kemetic Grid of Alchemy.',
      };
    default:
      return {
        pattern: SpeakerMeshPattern,
        opacity: 0.05,
        concept: 'The Sound System.',
      };
  }
};
